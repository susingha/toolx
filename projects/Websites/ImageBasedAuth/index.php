Purpose:
We are testing the usability and feasibility of image based passwords. The core idea is that with the string passwords its easy to hack by either beating the person up to reveal the secret or snooping though files etc. With image passwords, we test how well people can remember sequence of images which are not easily communicable. Below are the steps that you have to follow. Shouldn't take more than 10-15 mins.

Guide:
1. Go to the site http://supratik.comoj.com

2. For the first time you have to Sign up. Enter a Username, Name and
you have to set a password which would be a sequence of images chosen from the given list. You can select more than one image from the different category.

Note: You need to remember the images and the sequence you chose for yourself so chose a combination of any length that you can remember. Make it as secured as you would in real life. Ideally 2-6 images.

3. On the right side of the page these is a questionnaire. Kindly fill that. If you aren't an SBU student, you can leave the SBUID field blank.The description of the password is crucial so make it as descriptive as possible. (We want to measure how easily someone else can guess your password from your textual/verbal description.)

4. Click on submit on the bottom left. You will see
"Thank you for your registration. Please try logging in with your username and password. "

Relogin and The Fun part!!!! ..guessing other's passwords!!!

1. Give your username. On the next page give your password selection. Once you are in you can guess other's password.
2. Select a username. This will lead to the login screen. the next page would have the password description. Make your guess and see if you can succeed!

3. You can continue to guess and have fun!

The more the better!!!!
